let contador = 0
while(contador < 10){
    console.log(contador)
    contador++
}

// Do While 
// do...while loop: Similar al while loop, pero se ejecuta al menos una vez. Osea si o si se va a ejecutar
// el código una vez y ya después valida el while
do{
    console.log(contador);
    contador++;
}while (contador < 10);